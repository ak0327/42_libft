#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ret_ptr;
	size_t	i;

	ret_ptr = NULL;
	i = 0;
	while (i <= ft_strlen(s))
	{
		if (s[i] == (char)c)
			ret_ptr = (char *)&s[i];
		i++;
	}
	return (ret_ptr);
}
